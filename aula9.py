adicao = 10 + 10
print('Adição', adicao)

subtracao = 10 - 5
print('Subtração', subtracao)

multiplicacao = 10 * 10
print('Multiplicação', multiplicacao)

divisao = 10 / 2.2
print('Divisão', divisao)

divisao_inteira = 10 // 2.2  # corta as casas decimais
print('Divisão inteira', divisao_inteira)

exponeciacao = 2 ** 10  # 2 elevado a 10
print('Exponenciação', exponeciacao)

modulo = 55 % 2  # resto da divisão
print('Módulo', modulo)

#  é divisivel por 8:
print(10 % 8 == 0)
print(16 % 8 == 0)
#  é par ou impar:
print(10 % 2 == 0)
print(15 % 2 == 0)
