"""
    Python = Linguagem de Programação
    Tipo de tipagem = Dinâmica(sabe o tipo da informação que você passa pra ele) / Forte
    
    str -> string -> texto
    Strings são textos que estão dentro de aspas
"""
print(1234)  # ele ja sabe que é um numero inteiro

# Aspas simples
print('Carol Alves')
print(1, 'Carol "Alves"')
print(2, "Carol 'Alves'")

# Aspas duplas
print("Carol Alves")

# Escape
print("Carol \"Santos\"")

# r
print(r"Carol \"Santos\"")
